int	ft_atoi(char *str)
{
	int	i;
	int	sn;
	int	res;

	i = 0;
	sn = 1;
	res = 0;
	while (str[i] <= ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sn = sn * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return (res * sn);
}
