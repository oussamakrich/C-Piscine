int	ft_str_is_numeric(char *str)
{
	int	i;
	int	ref;

	i = 0;
	ref = 1;
	while (str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
		{
			ref = 0;
			break ;
		}
		i++;
	}
	return (ref);
}
