int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	ref;

	i = 0;
	ref = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 'A' || str[i] > 'Z')
		{
			ref = 0;
			break ;
		}
		i++;
	}
	return (ref);
}
